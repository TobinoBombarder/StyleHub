// Display all products
function displayProducts(products) {
    productsGrid.innerHTML = '';
    
    products.forEach(product => {
        productsGrid.innerHTML += `
            <div class="product-card" data-id="${product.id}">
                <img src="${product.images[0]}" alt="${product.title}" class="product-image">
                <div class="product-info">
                    <h3 class="product-title">${product.title}</h3>
                    <p class="product-price">${product.priceRange}</p>
                    <a href="#" class="view-btn" data-id="${product.id}">View Details</a>
                </div>
            </div>
        `;
    });
    
    // Add event listeners to view buttons
    document.querySelectorAll('.view-btn').forEach(btn => {
        btn.addEventListener('click', function(e) {
            e.preventDefault();
            const productId = parseInt(this.getAttribute('data-id'));
            showProductDetail(productId);
        });
    });
}

// Show product detail modal
function showProductDetail(productId) {
    const product = sneakers.find(p => p.id === productId);
    
    if (product) {
        // Generate gallery thumbs
        let thumbsHTML = '';
        product.images.forEach((img, index) => {
            thumbsHTML += `
                <img src="${img}" class="thumb ${index === 0 ? 'active' : ''}" 
                     onclick="changeMainImage(this, '${img}')">
            `;
        });
        
        // Generate affiliate links
        let linksHTML = '';
        product.links.forEach(link => {
            linksHTML += `
                <a href="${link.url}" target="_blank" rel="sponsored noopener" class="affiliate-link">
                    <i class="${link.icon}"></i> ${link.site}
                    <span class="price-tag">${link.price}</span>
                </a>
            `;
        });
        
        productDetail.innerHTML = `
            <div class="product-gallery">
                <img src="${product.images[0]}" class="gallery-main" id="main-image">
                <div class="gallery-thumbs">
                    ${thumbsHTML}
                </div>
            </div>
            <div class="product-details">
                <h2>${product.title}</h2>
                <p class="product-price">${product.priceRange}</p>
                <p>${product.description}</p>
                
                <div class="affiliate-links">
                    <h3>Buy From:</h3>
                    ${linksHTML}
                </div>
            </div>
        `;
        
        productModal.style.display = "block";
    }
}

// Change main image in gallery
function changeMainImage(thumb, imgSrc) {
    document.getElementById('main-image').src = imgSrc;
    document.querySelectorAll('.thumb').forEach(t => t.classList.remove('active'));
    thumb.classList.add('active');
}

// Filter and sort products
function filterAndSort() {
    let filtered = [...sneakers];
    
    // Filter by brand
    if (filterBy.value !== 'all') {
        filtered = filtered.filter(p => p.brand === filterBy.value);
    }
    
    // Search by title
    if (searchInput.value) {
        const searchTerm = searchInput.value.toLowerCase();
        filtered = filtered.filter(p => 
            p.title.toLowerCase().includes(searchTerm) || 
            p.description.toLowerCase().includes(searchTerm)
        );
    }
    
    // Sort products
    switch (sortBy.value) {
        case 'price-low':
            filtered.sort((a, b) => {
                const priceA = parseInt(a.links[0].price.replace('$', ''));
                const priceB = parseInt(b.links[0].price.replace('$', ''));
                return priceA - priceB;
            });
            break;
        case 'price-high':
            filtered.sort((a, b) => {
                const priceA = parseInt(a.links[0].price.replace('$', ''));
                const priceB = parseInt(b.links[0].price.replace('$', ''));
                return priceB - priceA;
            });
            break;
        case 'popular':
            filtered.sort((a, b) => b.popularity - a.popularity);
            break;
    }
    
    displayProducts(filtered);
}

